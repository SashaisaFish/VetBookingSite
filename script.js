// create calendar days

const calendar = document.getElementById("calendar-container");

for (let i = 1; i <= 31; i++) {
  // create div element day
  const day = document.createElement("div");
  // add attribute id `day ${i}`
  day.setAttribute("id", `day-${i < 10 ? `0${i}` : i}`);
  // add classes day mali border-thin entry
  day.classList.add("day", "mali", "border-thin", "entry");

  // add p element num
  const num = document.createElement("p");
  // add class number
  num.classList.add("number");
  // add inner text i
  num.innerText = i;
  // add num to day
  day.append(num);
  // add day to calendar
  calendar.append(day);
}

const form = document.getElementById("your-pet");
// when form submitted, get day from form and run
form.addEventListener("submit", (event) => {
  // prevent page from refreshing on submit
  event.preventDefault();

  // save data from doc as variables
  const data = new FormData(form);
  // get date as day from form
  const date = data.get("ap-date");
  const dd = `${date[8]}${date[9]}`;

  // run function bookSlot
  bookSlot(dd);
});
// create function
function bookSlot(dd) {
  // redeclare data
  const data = new FormData(form);
  // create div element bookedSlot
  const bookedSlot = document.createElement("div");
  // add classes booked-slot border-thin mali dark to bookedSlot
  bookedSlot.classList.add(
    "booked-slot",
    "mali",
    "border-thin",
    "dark",
    "light-font"
  );
  // find day with id dd
  const dayDD = document.getElementById(`day-${dd}`);
  // append bookedSlot to dayDD
  dayDD.append(bookedSlot);
  // read name and id from form
  const petName = data.get("pet-name");
  const petId = data.get("pet-id");
  const petSpecies = data.get("species");
  // create div elements petNameDiv, timeDiv and detailsDiv
  const petNameDiv = document.createElement("div");
  const timeDiv = document.createElement("div");
  const detailsDiv = document.createElement("div");
  // give divs attribute IDs
  petNameDiv.setAttribute("id", `pet-${petName}`);
  timeDiv.setAttribute("id", data.get("ap-time"));
  detailsDiv.setAttribute("id", `details${petId}${petSpecies}`);
  // create p elements for details ID and Species
  const petIdP = document.createElement("p");
  const petSpeciesP = document.createElement("p");
  // give inner text to all but image and price
  petNameDiv.innerText = petName;
  timeDiv.innerText = data.get("ap-time");
  petIdP.innerText = petId;
  petSpeciesP.innerText = petSpecies;
  // append divs to bookedSlot
  bookedSlot.append(petNameDiv, timeDiv, detailsDiv);

  // get image source and price from object based on id and species
  getInfo(petSpecies, petId);

  // make link
  const bookedLink = document.createElement("a");
  bookedLink.innerText = 'view'
  bookedLink.setAttribute("href", "petCard.html");
  bookedLink.setAttribute("target", "_blank");
  bookedSlot.append(bookedLink)
  bookedLink.addEventListener("click", () => {
    const petNameLink = document.getElementById("pet-name-link");
    const petIdLink = document.getElementById("pet-id-link");
    const petSpeciesLink = document.getElementById("pet-species-link");
    const petImageLink = document.getElementById("pet-image-link");
    const priceLink = document.getElementById("price-link");
    const timeLink = document.getElementById("time-link");

    const petImageCard = document.getElementById('pet-image')
    const petPrice = document.getElementById('price')
    
    petNameLink.append(petNameDiv)
    petIdLink.append(petIdP)
    petSpeciesLink.append(petSpecies)
    petImageLink.append(petImageCard)
    priceLink.append(petPrice)
    timeLink.append(timeDiv)

});
}

// function to get image source and price from object based on id and species
function getInfo(petSpecies, petId) {
  if (petSpecies === "cat") {
    species = cat;
  } else if (petSpecies === "dog") {
    species = dog;
  } else if (petSpecies === "reptile") {
    species = reptile;
  } else if (petSpecies === "bird") {
    species = bird;
  } else if (petSpecies === "largeCat") {
    species = largeCat;
  } else if (petSpecies === "smallAnimal") {
    species = smallAnimal;
  }
  // find correct object based on ID and species
  const pet = species.find(({ ID }) => ID === petId);
  console.log(pet);
  // console.log(pet.Price)
  // console.log(pet.Image)
  const price = pet.Price;
  const image = pet.Image;
  // create elements for price text and image
  const priceText = document.createElement("p");
  const petImage = document.createElement("img");
  // set element IDs
  priceText.setAttribute("id", "price");
  petImage.setAttribute("id", "pet-image");
  // set priceText
  priceText.innerText = price;
  // set petImage source
  petImage.setAttribute('src', image)
  // append to details
  const detailsDiv = document.querySelector(`details${petId}${petSpecies}`);
  detailsDiv.append(priceText, petImage);

  console.log(priceText.innerText);
  console.log(petImage.src);
}

